const User = require('./../models/userModal');
const catchAsync = require('../utils/catchAsync');

exports.getAllUsers = catchAsync(async (req, res) => {
  const users = await User.find();

  // ---- SEND RESPONSE ----
  res.status(200).json({
    status: 'success',
    requestAt: req.requestTime,
    results: users.length,
    data: {
      users: users,
    },
  });
});

